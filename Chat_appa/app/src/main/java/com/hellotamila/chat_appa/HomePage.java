package com.hellotamila.chat_appa;

import android.content.Intent;
import android.os.Bundle;
import android.support.v7.app.AppCompatActivity;
import android.view.View;
import android.widget.EditText;
import android.widget.Toast;

public class HomePage extends AppCompatActivity {

    EditText xEdtMobileNo;
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.homepage);
        xEdtMobileNo = (EditText) findViewById(R.id.mobileno);

    }
    public void login(View v) {
        if(xEdtMobileNo.getText().toString().length()!=10)
        {
            xEdtMobileNo.setError("Digit Should be 10");
            return;
        }
        Intent intent=new Intent(HomePage.this,MainActivity.class);
        intent.putExtra("mobileno", xEdtMobileNo.getText().toString());
        startActivity(intent);
        //Toast.makeText(getApplicationContext(),"Entered",Toast.LENGTH_LONG).show();
    }
}















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































